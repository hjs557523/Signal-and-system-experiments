clear all;
close all;
R=0.02;
t= -2:R:2;
f=stepfun(t,-1)-stepfun(t,1);
f1=f.*exp(-j*5*t);
f2=f.*exp(j*5*t);
N=200;
W1=5*pi;
k=-N:N;
W=k* W1/N;
F1=f1*exp(-j*t'*W)*R;
F2=f2*exp(-j*t'*W)*R;
F1=real(F1);
F1=real(F2);
subplot(2,1,1);
plot(W,F1);
xlabel('w');
ylabel('F1(jw)');
title('ÆµÆ×F1(jw)');
subplot(2,1,2);
plot(W,F2);
xlabel('w');
ylabel('F2(jw)');
title('ÆµÆ×F2(jw)');